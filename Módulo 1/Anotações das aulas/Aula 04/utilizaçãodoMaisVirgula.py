print(7 + 4)  # 7 + 4 = 11 #soma dos números

print('7' + '4')  # '7' + '4' = '74' #junta os númeors 

print('7', '4')  # '7' , '4' = '7 4' #separa eles

""" NOTA: Uma mensagem e um número é possivel no mesmo print utilizando a vírgula, mas não o símbolo de "+".
Usando a vírgula, adiciona-se um espaço entre os valores dentro do print."""

print('Olá', 5)  # 'Olá' , 5 = 'Olá 5'
